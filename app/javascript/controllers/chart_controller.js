let remaining_calories = parseInt(document.getElementById("remaining_calories").innerHTML)
let daily_calorie_target = parseInt(document.getElementById("daily_calorie_target").innerHTML)

function setProgress(percent) {
  const circle = document.getElementById("progress");
  const circumference = 251.2;
  const offset = circumference - (percent / daily_calorie_target) * circumference;
  circle.style.strokeDashoffset = offset;
}

let progress = daily_calorie_target;
const target = remaining_calories;
const interval = setInterval(() => {
  if (progress >= target) {
      clearInterval(interval);
  } else {
      progress++;
      setProgress(progress);
  }
}, 200);
